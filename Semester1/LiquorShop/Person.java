package Semester1.LiquorShop;

public class Person {
    private int age;
    private boolean hasMoney;

    public Person(int age, boolean hasMoney) {
        this.age = age;
        this.hasMoney = hasMoney;
    }

    public int getAge() {
        return age;
    }

    public boolean getHasMoney() {
        return hasMoney;
    }

}
